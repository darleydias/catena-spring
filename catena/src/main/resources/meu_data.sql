CREATE TABLE tb_comarcas (
  ID INTEGER PRIMARY KEY AUTO_INCREMENT,
  NOME VARCHAR(255),
  TIPO VARCHAR(255),
  CREATEDAT DATE
);
CREATE TABLE tb_promotor (
  ID INTEGER PRIMARY KEY AUTO_INCREMENT,
  `nome` VARCHAR(255),
  `mamp` VARCHAR(255),
  `sexo` VARCHAR(255)
);
CREATE TABLE `tb_orgao` (
  `id` INTEGER PRIMARY KEY AUTO_INCREMENT,
  `descri` VARCHAR(255)
);
CREATE TABLE `tb_funcao` (
  `id` INTEGER PRIMARY KEY AUTO_INCREMENT,
  `descri` VARCHAR(255),
  `orgao_id` INTEGER
);